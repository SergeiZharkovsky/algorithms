class DoubleConnectedNode:
    def __init__(self, value, next=None, prev=None):
        self.value = value
        self.next = next
        self.prev = prev


def solution(node):
    while node.next is not None:
        node = node.next
    new_node = node
    while node.prev is not None:
        head = node.prev
        node.prev = node.next
        node.next = head
        node = head
    return new_node


